package question3;

public class SwapFunction {

	public static void main(String[] args) {
		
		int first = 15;
		int second = 10;
		
		first = first + second; //15
	    second = first - second;//15-10 = 5
	    first = first - second;//15-5 =10
	    System.out.println("first :"+first);
	    System.out.println("second :"+second);

	}
}
